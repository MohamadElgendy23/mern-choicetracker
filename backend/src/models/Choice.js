import mongoose from "mongoose";

const choiceSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    category: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Category",
    },
  },
  { timestamps: true }
);

const Choice = mongoose.model("Choice", choiceSchema);

export default Choice;
